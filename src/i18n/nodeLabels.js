// Node labelini joriy tilga o'girish
export function translateNode(node, lang) {
  const entry = NODE_LABELS[node.id]
  if (!entry) return node // tarjima yo'q = o'zgarmasdan qoladi
  return { ...node, label: entry[lang] || entry.uz || node.label }
}

// Butun tree ni tarjima qilish
export function translateTree(node, lang) {
  const translated = translateNode(node, lang)
  return {
    ...translated,
    children: (node.children || []).map(c => translateTree(c, lang))
  }
}
